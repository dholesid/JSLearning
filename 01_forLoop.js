console.log(`================= for loop ================\n`);
console.log(`Numbers incrementing 5 to 15 by 1\n` );
for (let i= 5; i<=15; i++) {
console.log(i);
}
console.log(`_______________________________________________________________________________________________________\n`);

console.log(`Numbers decrementing 50 to 40 by 1 \n`);
for (let i= 50; i>=40; i--) {
    console.log(i);
    }
console.log(`_______________________________________________________________________________________________________\n`);

console.log(`First 15 odd numbers`)
for (let i=1; i<=20; i++) {
    if (i%2!=0)
     {
        console.log(i)
     }
    }
console.log(`_______________________________________________________________________________________________________\n`);

console.log(`First  10 even numbers\n`)
for (let i=1; i<=20; i++) {
    if (i%2==0)
     {
        console.log(i);
     }
    }
console.log(`_______________________________________________________________________________________________________\n`);

console.log(`Table of 5\n`)
for (let i= 5; i<=50; i=i+5) {
    console.log(i);
     }
console.log(`_______________________________________________________________________________________________________\n`);

console.log(`Table of 10\n`)
for (let i= 10; i<=100; i=i+10) {
    console.log(i);
     }
console.log(`_______________________________________________________________________________________________________\n`);

console.log(`Table of 10 in reverse order 10\n`);
for (let i= 100; i>=10; i=i-10) {
    console.log(i);
     }
console.log(`_______________________________________________________________________________________________________\n`);